.text
.globl main

main:
    # 初始化
    li $t0, 0x1234   # 要存储的值
    li $s0, 0            # 测试结果标志（0=失败，1=成功）

    # 执行 sw 和 lw
    sw $t0, 0x400($zero)   # 写入内存地址 0x400
    lw $t1, 0x400($zero)   # 从同一地址读回

    # 比较读回的值是否等于原始值
    sub $s0, $t0, $t1       # 如果相等，结果为 0
    # 注意：若 $s0 == 0 → 测试通过；若 $s0 != 0 → 测试失败

    li $v0, 10
    syscall